    private void imageAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_imageAddActionPerformed

        JFileChooser fc = new JFileChooser();
        fc.showOpenDialog(this);
        File selectedImage = fc.getSelectedFile();
        path = selectedImage.getName();
        
        try {
            Image img = ImageIO.read(selectedImage);
            productPic.setIcon(new ImageIcon(img.getScaledInstance(productPic.getWidth(),
                    productPic.getHeight(), Image.SCALE_SMOOTH)));
        } catch (Exception ex) {
            System.out.println(ex.getStackTrace());
        }
    }//GEN-LAST:event_imageAddActionPerformed

    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed

        
        String name=nameText.getText().toString();
        int price=Integer.parseInt(priceText.getText());
        int quantity=Integer.parseInt(quantityText.getText());
        String brand=brandText.getText().toString();
        String category=null;
        if(categoryComboBox.getSelectedItem().equals("Mobile"))
            category="Mobile";
        else
            if(categoryComboBox.getSelectedItem().equals("Laptop"))
                category="Laptop";
            else
                if(categoryComboBox.getSelectedItem().equals("Tv"))
                    category="Tv";
                else
                    if(categoryComboBox.getSelectedItem().equals("Clothing"))
                        category="Clothing";
                    else
                        if(categoryComboBox.getSelectedItem().equals("Vehicles"))
                            category="Vehicles";
                        else
                                if(categoryComboBox.getSelectedItem().equals("Books"))
                                    category="Books";
                                else
                                    if(categoryComboBox.getSelectedItem().equals("Furniture"))
                                        category="Furniture";
        String descriptipon=descriptionText.getText();
        String dateOfManufacture=dateOfManufactureText.getText();
        int productId=itemDatabase.getProductId(name);
        productModel model=itemDatabase.getItemInformationByProductId(productId);
        double offers=0;
        itemDatabase.insertIntoDatabase(name, price, quantity, brand, category, descriptipon, path,productId,offers,dateOfManufacture);
        
        
        
        
        
    }//GEN-LAST:event_submitButtonActionPerformed

